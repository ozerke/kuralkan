        <p>Bilişim ve Otomotiv (Motosiklet) sektörünün öncü kuruluşlarından birisidir.&nbsp;</p>

        <p>Grubumuzun Otomotiv sanayinde motosiklet sektörüne ilk adımını 1987 yılında MZ Motosikletlerin Türkiye
            Distribütörlüğe ile atmıştır.</p>

        <p>1989 yılında Demirperde ülkelerinde yaşanan gelişmeler ve Batı Almanya ile Doğu Almanya’nın birleşmesi ile
            beraber MZ fabrikasını Doğu Almanya’dan satın alarak fabrikayı bir sene içinde çok ilginç ve büyük bir
            başarı hikayesi ile Türkiye’ye taşınıştır.</p>

        <p>Sektörde gösterilen başarının ardından, 1994 yılında İstanbul Dudullu’daki fabrikasında % 100 yerli MZ
