            motosikletleri hem yurt içi hem yurt dışı pazara sunmaktadır.</p>

        <p>Başarı grafiğini 2000’li yıllarda da taşıyan Kuralkan A.Ş. faaliyetini 2006 yılında hizmete açılan İstanbul
            Sabiha Gökçen Havaalanı yakınındaki Tuzla-Tepeören mahallesinde Kanuni Motosiklet Üssünde sürdürmektedir.
        </p>

        <p>Kanuni Motosiklet Üssü 60.000 m2 lik kapalı alanıyla yakın coğrafyanın en büyük motosiklet üretim
            tesislerinden biri konumundadır.</p>

        <p>Motosiklet üretimindeki en yeni teknolojileri ve yenilikleri bünyesinde barındırmak üzere tasarlanan fabrika
            içerisinde; eğitim pisti, sürüş test alanı, depolama, montaj ve çok kapsamlı bir kalite kontrol ünitesi
            bulunmaktadır</p>

        <p>Kanuni Motosiklet Üssü günlük 1000 motosiklet üretim kapasitesine sahiptir.&nbsp;</p>

        <p>Yerli üretimin yanı sıra uluslararası ortaklıklar ve stratejik iş birliği projeleriyle de motosiklet
            sektöründeki gücünü ve etkinliğini pekiştiren Kuralkan A.Ş. 2013 yılından itibaren Dünyanın 3’üncü büyük
            motosiklet firması Hindistanlı Bajaj Auto ile ortaklık anlaşması imzalamıştır. SPORT segmentinde büyük bir
            başarı yakalanmış, binlerce motorsever yüksek teknoloji ürünlü motorlara uygun fiyatlarla kavuşmuşlardır.
